#Operators
a,b=56,78
#Arithmetic Operators(+,-,*,/,%,//,**)

print("Arithmetic:")
print("Addition:",a+b)
print("Subtraction:",a-b)
print("Multiplication:",a*b)
print("Division:",a/b)
print("Modulus:",a%b)
print("Floor Division:",a//b)
print("Power:",a*b)

#Logical operators(AND,OR,NOT)
print("\nLogical:")
print(a>100 and b<90)
print(a>b or b<50)
print(not(a>b))

#Comparision Operators(==,!=,>,<,>=,<=)
print("\nComparision")
print("Equal:",a==b)
print("Not Equal:",a!=b)
print("Greater Than:",a>b)
print("Less Than:",a<b)
print("Greater or Equal:",a>=b)
print("Less than or Equal:",a<=b)

#Assignment Operators(+=,-=,*=,..)
print("\nAssignment:")      
a+=5 
print("a+=:",a)
b*=17
